#include "holberton.h"
/**
 *print_sign - function that display the sign with absolute value 
 *@n: int
 *Return: 0
 */
int print_sign(int n)
{
	if (n > 0)
	{
		_putchar('+');
		return (1);
	}
	if (n < 0)
	{
		_putchar('-');
		return (-1);
	}
	if (n == 0)
	{
		_putchar('0');
		return (0);
	}
	return (0);
}
